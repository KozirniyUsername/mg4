public class MyGame4 extends ApplicationAdapter {
	SpriteBatch batch;

	static World world;

	@Override
	public void create () {
		batch = new SpriteBatch();

		world = new World(new Vector2(0,-10),true);
	}

	@Override
	public void render () {
		ScreenUtils.clear(0, 0, 0, 1);

		batch.begin();

		batch.end();

		world.step(1,6,2);
	}
	
	@Override
	public void dispose () {
		batch.dispose();

	}
}
